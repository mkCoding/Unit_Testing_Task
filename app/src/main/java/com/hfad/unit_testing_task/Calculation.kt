package com.hfad.unit_testing_task

class Calculation {

    fun addition(x:Int,y:Int):Int{
        return x + y
    }

    fun subtraction(x:Int, y:Int):Int{
        return x - y
    }

    fun division(x:Int, y:Int):Int{
        return x / y
    }

    fun multiply(x:Int, y:Int):Int{
        return x * y
    }

    fun squared(x:Int):Int{
        return x * x
    }

    fun cubed(x:Int):Int{
        return x * x * x
    }

}